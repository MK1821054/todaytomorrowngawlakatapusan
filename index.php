     In the time when we should be saving lives, people are being killed either by incompetence
	 or by the bullet. In a country already heavily afflicted with the COVID-19 pandemic, this
	 senseless killings shows the inconsiderate of the Duterte administration and his failed
	 leadership to solve the root of this problem which is the poverty, inequality of the people
	 in our nation, and misplaced priorities of our government spending resources to hunt down
	 activists while thousands of our countrymen are dying because of hunger or the virus. Hence,
	 they should have minimized their militarism on handling this pandemic because people will only
	 be more afraid than they are now. As a matter of fact, there are alternatives and programs
	 to discipline people without being militaristic to the extent of killing and shaming violators.
