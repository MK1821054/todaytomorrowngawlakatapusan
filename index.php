     President Rodrigo Duterte has faced the Coronavirus pandemic in a decidedly militaristic
     fashion. The President also had a message for anyone who intended to repeat the action of San
     Roque residents (leftists): <i>“I will not hesitate. My orders are to the police and
     military, as well as village officials if there are any trouble, or occasions where there’s
     violence and your lives are in danger: shoot them dead.”</i>
<p>
<br>
<br>
<p align="center">
