     Series of incompetent responses were executed by the government and their continuous
     deprioritization leads the state to rapidly increase the Coronavirus cases in the
     Philippines. As the arising of this worldwide pandemic crisis, the Philippine government
     makes one of the worst examples of handling the pandemic due to its inefficiency married
     with militarism threaded throughout its responses. It is a bleak one marred by a totalitarian
     streak and the people’s increased realization that the state is more intent on filling jails.
